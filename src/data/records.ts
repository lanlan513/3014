export const getRecordById = (id: string): Record | undefined => {
  return records.find((r) => r.id === id);
};

export const getRecordsByMood = (mood: MoodId): Record[] => {
  return records.filter((r) => r.mood === mood);
};

export const getRecommendedRecords = (): Record[] => {
  return records.filter((r) => r.recommended);
};

export const getRelatedRecords = (currentId: string, limit = 3): Record[] => {
  const current = getRecordById(currentId);
  if (!current) return [];
  return records
    .filter((r) => r.mood === current.mood && r.id !== currentId)
    .slice(0, limit);
};
